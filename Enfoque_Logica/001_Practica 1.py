# Tema: Lógica Proposicional
# Este programa evalúa una fórmula lógica simple: (p ∧ ¬q) ∨ r

# Se definen las proposiciones como booleanos
p = True
q = False
r = False

# Se aplica la fórmula lógica (p AND NOT q) OR r
resultado = (p and not q) or r

# Se muestra el resultado de la fórmula
print("Resultado de (p ∧ ¬q) ∨ r:", resultado)
